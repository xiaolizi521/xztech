<b>The Mission:<br />
</b>&nbsp;The purpose and mission of 'Spread the Bleach!' is to try and spread the knowledge and popularity of the Bleach Anime and Manga to all anime fans on the internet, (or at least a huge chunk of them!). We here at Bleach7.com want everyone to be able to read and learn about BLEACH. That's where 'Spread the Bleach!', comes into play.<br />
<br />
<b>The Goal:</b><br />
&nbsp;Our goal for the 'Spread the Bleach!' campaign is to spread information about BLEACH to 15,000 new users, in a set amount of time, through referrals of our current members.<br />
&nbsp;We will try to meet this goal of 15,000 new users through Spread The Bleach each month and reset at the beginning of each month. The top referrer(s) may start to receive a special gift or prize.<br />
<br />
<b>How It Works:</b><br />
&nbsp;Staff and Fan Members alike, here at Bleach 7 and Bleach Forums; will use a single referral link (visit the referral link), to spread the word about BLEACH, using Bleach7.com as well as the forums. The referral link points new users to a page devoted only to the introduction of the BLEACH series, (both anime and manga will be covered on this page). So hopefully, the user that visits through this link, and reads the page; will become an avid BLEACH series fan, and continue to read and download and learn about BLEACH, as well as possibly share the <i>good news</i>, with other anime fans they know.<br />

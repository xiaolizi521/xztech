The sound of beeps echoed through the air, their pattern irregular, as if someone was pulling back on the sound. Stark, desolate walls encompassed the room, suffocating any tang of personality it contained. The air was stained with the bitter taste of disinfectants, along with the aftertaste of death. An aura of hopelessness, bereavement and suffering wavered throughout.<br />
<br />
An undistinguishable mass of coarse linen sheets covered a bed, pertaining to the white regimen of the room. Grey hair streaked with the orange color of youth was splayed across a pillow. Its texture was rough, as expected with hair of the wizened.<br />
<br />
Time had played its mischievous game against Ichigo. Disease had spread throughout his bones, gradually diffusing its poison to other vital parts.<br />
<br />
He wasn't scared, though…<br />
<br />
He was ready to die…<br />
<br />
It had been so long since his ordeal with shinigami and the soul society. The disturbance had vanished as quickly as it had appeared, leaving him befuddled and craving.<br />
<br />
Craving for her…<br />
<br />
Dark eyes closed in fatigue, as he began to think…<br />
<br />
Ichigo POV…<br />
<br />
It was difficult for me to realize I loved her…<br />
<br />
She emerged in my life, throwing everything out of equilibrium.<br />
<br />
There was no way to describe her. She was like the forbidden fruit, the legendary ambrosia of the gods. Something I could never posses…<br />
<br />
She was just…Rukia.<br />
<br />
Extraordinary beauty did not belong to her, but she was gorgeous in her own perfect way.<br />
<br />
It was the little habits, the little quirks—the small things that made Rukia her own person.<br />
<br />
It was the way her smooth brow wrinkled ever so slightly, as she often frowned at me.<br />
<br />
It was the times where she would lift the corners of her soft, tender mouth into a composed smile.<br />
Or, the way she walked. Moving, almost floating with such poise other women would only dream of having.<br />
<br />
Rukia…my Rukia….<br />
<br />
End POV…<br />
<br />
A slight smile found itself creeping upon Ichigo's haggard face. Distinct memories of the past flooded his mind, as he continued to contemplate. One unique memory shone brightly in his mindscape—the memory of his first and last Christmas with Rukia.<br />
<br />
Flashback…<br />
<br />
Wispy ice crystals danced through the air as Ichigo emitted a deep breath in the brisk winter weather. Gloved hands shuffled through the small pocket of a thick coat, almost struggling to find the item of its desire. A sigh of relief was heard as the object, a weathered key, was found. The key soon found itself unlocking the thick wooden door.<br />
<br />
&quot;Okaeri…&quot; he mumbled softly, despite the fact that his family was no where to be seen.<br />
<br />
A pleasant female voice coming from upstairs responded to his call.<br />
<br />
&quot;Ah…Okaeri, Ichigo…&quot;<br />
<br />
Ichigo's brown-hued eyes glanced at the decorated tree situated in the center of the living room. A small smile flittered across his face, surprised at how quick his father and sisters managed to beautifully set up the Christmas tree.<br />
<br />
&quot;Rukia…? Could you come down here for a sec', there's something I want you to see.&quot; he grinned, &quot;Don't worry, neither my father nor my sisters are here. They've gone out to a small party.&quot;<br />
<br />
The usual sound of a squeaking stairs gave notice to Rukia's arrival. Graceful bare feet treaded slowly down the staircase. Lavender eyes glanced at Ichigo, their stare filled with confusion. Ichigo merely shrugged, pointing a long finger at the Christmas tree. Rukia glanced at the ornate display set in the family room. Her eyes widened at the sight, never before seeing such a thing.<br />
<br />
&quot;It's…beautiful…&quot; she whispered whole-heartedly.<br />
<br />
Slowly Rukia walked over to the immense tree. Her hand extended to touch the evergreen boughs, but stopped short. It was as if the tree was just too sacred to lay a single finger upon.<br />
<br />
The tree was intertwined with strings of vivid, multihued lights—their luminosity surrounding the green giant in a faint glow. Elaborate and simple glass orbs enveloped it perfectly, creating a clear image of simple elegance. Boxes wrapped with copious amounts of decorated paper and colorful ribbons lay in piles underneath the green canopy. The fresh, unique scent of pine wafted throughout the room, settling into every nook and crack.<br />
<br />
Rukia finally broke the silence of the room.<br />
<br />
&quot;Ichigo…why is this tree here? What type of strange human tradition is this?&quot;<br />
<br />
&quot;It's called Christmas. The holiday is more of an American tradition, but Japanese still celebrate it. Dad goes to extreme with Christmas because…my mom used to cherish this holiday.&quot; Ichigo stated, trying to hold his tough persona even when speaking about his mother.<br />
<br />
Rukia's face dropped slightly at the mention of Ichigo's mother. She sighed before speaking again.<br />
&quot;What is the purpose of this…Christmas?&quot; Rukia questioned, interested in all this new information she was taking in.<br />
<br />
&quot;It is a holiday to celebrate the birth of Jesus, but it has turned into more of a multi-cultural occasion.&quot; Ichigo shrugged, his gaze lingering on Rukia's meditative face.<br />
<br />
The pale blue hem of her dress dragged across the wooden floor as she slowly sauntered around the room. She gazed at the numerous decorations covering the wall and furniture, interested in every single detail. Rukia stopped moving, as an object caught her eye.<br />
<br />
The object was a branch swathed in mint-green leaves and berries, hanging precariously above her, with tape.<br />
<br />
&quot;Ahh…Ichigo, what is that?&quot;<br />
<br />
Rukia lowered her inquiring gaze, suddenly noticing that she had stopped right next to Ichigo. A rouge blush burned dimly upon Ichigo's normally stoic face. His left hand twitched ever so slightly, as if something strange had occurred.<br />
<br />
&quot;It's…that…ano….&quot; Ichigo grunted, shoving his embarrassment down. &quot;That is mistletoe, when two people meet underneath it….they kiss.&quot;<br />
<br />
&quot;Ah…Sou ka…&quot; Rukia whispered, her voice filled with underlying amusement.<br />
<br />
Rukia pushed herself onto her toes, leaning ever so gently toward Ichigo. Without any moment's notice, Rukia placed her soft lips against Ichigo's own. The kiss was evanescent, like the sudden flutter of a butterfly's wings. The brief meeting ended as Rukia took a step away from Ichigo, an undistinguishable look set on her face. The blaze burned ever so clear upon Ichigo's cheeks, his lithe fingers set on his mouth in wonder. Something filled the cavity of his chest—no, it was not a bad feeling at all, it was a remarkable feeling.<br />
<br />
The temporary shinigami and ex-shinigami faced each other, silence echoing throughout the room. No words were needed to be spoken, for the pair knew that this was something that could never be explained.<br />
<br />
Rukia made her way back up the staircase, a hidden smile resting on her lips.<br />
<br />
End Flashback…<br />
<br />
Ichigo coughed hoarsely, his lungs heaving as he did so. He layed there attempting to gasp for air, but he was failing badly. Rare tears filled his deep brown eyes, as Ichigo finally realized it was his time.<br />
<br />
&quot;Ichigo, you idiot! What are you doing just lying there? We have hollows to kill, and souls to save.&quot;<br />
<br />
Ichigo's eyes shot wide open, fully recognizing that voice. It was…it was…<br />
<br />
Rukia…<br />
<br />
It was then that Ichigo realized that he was floating steadily above his deceased corpse. Looking down, he saw himself clothed in the garments of a shinigami; his Zangetsu strapped proudly to his back. Ichigo's soul displayed the picture of a young shinigami, not the sight of an aged, dying man.<br />
<br />
Slowly, he switched his view over to the area where Rukia's voice came from. His mouth layed slack-jaw as he saw her…Rukia…<br />
<br />
She looked exactly like the last time he had seen her. Silky, black hair draping slightly over her shoulders, pale, white skin showing through the openings of her kimono, and unique, violet eyes glittering with mirth.<br />
<br />
A hand was placed on her slim waist, as Rukia waited impatiently for Ichigo's bout of nostalgia to pass.<br />
<br />
The ringing of a cell phone reverberated through the folds of her kimono. She pulled out the silver electronic with ease, flipping its cover up.<br />
<br />
&quot;Hollow appearance on North Mitsui Ave…&quot; she smiled, &quot;We'd better get going…&quot;<br />
<br />
Ichigo responded with one of his atypical smiles, slowly treading behind Rukia.<br />
<br />
‘Just like old times…' he thought.<br />
<br />
Rukia turned around to face the approaching Ichigo, her right hand grabbing the right arm of his kimono. She leaned toward Ichigo, reminiscent of that old Christmas memory. This time though, Ichigo knew what to do.<br />
<br />
A chaste kiss occurred between their two lips, lasting for what seemed forever for the two shinigami.<br />
<br />
&quot;I love you…&quot; whispered Ichigo.<br />
<br />
Rukia smiled. &quot;I think I've always known…&quot;</span>
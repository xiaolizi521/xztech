<b>Q. What do &quot;RAW&quot; and &quot;SUB&quot; mean?</b><br />
<br />
Raw refers to an anime episode (or manga chapter) that is unaltered, and still in Japanese.  The sub of an episode means it has had English (or another language) subtitles added to it.  The sub of a manga chapter (also called a scanlation) means the manga has been cleaned and the Japanese text has been replaced with an English translation.<br />
<br />

<b>Q. When does the anime get released?</b><br />
<br />
The raw of the anime is usually out within 5-6 hours of airing in Japan, and the sub will follow at around 16-20 hours after airing in Japan. The speed of the raw release depends on the distributor of the RAW.  So for a low quality raw, one would look to people such as <a href="http://bt.saiyaman.info/" target="_blank">Saiyaman</a>.  However for a quality RAW, one would look to people such as <a href="http://www.l33t-raws.org/" target="_blank">l33t RAWS</a>.  For subbers, there are a few options.  The speed subbers, such as <a href="http://www.dattebayo.com" target="_blank">Dattebayo</a>, can have a release either by Wednesday night or early Thursday morning.  Their have been several slower, more careful subbing groups to have subbed Bleach and there has been debates as to who is better.  We ask that you discourage from the argument, since most people have heard this debate week-after-week.<br /><br />
<b>Q. What about the manga?</b><br />
<br />
The schedule for the manga is a little less predictable. The Bleach Manga is usually available on Fridays although it may vary from week to week.  Since the manga is more elusive in people releasing, one cannot put a definitive date as to when they release.  Plus, sometimes Shounen Jump, the magazine that Bleach is published in, takes a week off every so often, so it is really hard to say.  As for people scanlating the series, there are several groups out there scanlanting the series.  Here at Bleach7.com, we have our own group called Manga7.  However in the Manga Scanlating community, they are better known as <a href="http://www.maximum7.net/" target="_blank">Maximum7</a>.  Maximum7 is a High Quality scanlating group.  They generally have a scanlated version within a few days of the first available RAW.  Another team that is working on Bleach, that is not affiliated with Bleach7, is Manga-Rain.<br /><br />

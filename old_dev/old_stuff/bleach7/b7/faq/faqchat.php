<b>Q. Why is my nickname Bleacher### instead of what I typed in?</b><br />
<br />
This means that someone on irchighway (the network our chatroom is run on) is already using your nickname. There is a box in the bottom right corner of the chatroom that allows you to pick a new nickname.
<br /><br />
<b>Q. Why did my nickname get changed to Guest#####?</b><br />
<br />
Someone on irchighway has already registered your nickname, but unlike the case with the Bleacher### nick change, the person is not currently online. If you don't enter the password for the nickname within one minute, it changes your nickname to Guest#####. Enter a new nickname in the bottom right text box.
<br /><br />
<b>Q. How do I know if the nickname I have chosen is already registered?</b><br />
<br />
Nickserv, the service that manages all of the nicknames, will send you this message: <br /><br />&nbsp;&nbsp;&nbsp;-NickServ- This nickname is registered and protected.  If it is your 
<br />&nbsp;&nbsp;&nbsp;-NickServ- nick, type <b>/msg NickServ IDENTIFY <u>password</u>.</b>  Otherwise, 
<br />&nbsp;&nbsp;&nbsp;-NickServ- please choose a different nick. 
<br />&nbsp;&nbsp;&nbsp;-NickServ- If you do not change within one minute, I will change your nick.<br /><br />
If you don't see this message, then you are probably fine.<br />
<br />
<b>Q. How do I register my nickname?</b><br />
<br />
In the text prompt, type "/msg nickserv register &lt;password&gt; &lt;email address&gt;"  The &lt;password&gt; is the password you wish to use to lock that nick to you.  The email address is where your password will be sent in case you forget it. So if my password is abc, I would type "/nickserv register abc bleach7@bleach7.com"  Now here is the catch: you have to identify yourself every time you sign into the chatroom.  To do so, all you have to do is type "/msg nickserv identify &lt;password&gt;". All you are doing is sending a quick message to the nick server saying you are the owner of the nickname.<br />
<br />
<b>Q. I have to identify every time log in?  Why bother registering then?</b><br />
<br />
Actually, register has it's perks. Registering prevents other users from stealing your nickname and pretending to be you, like registering at a forum. Also, if a user who is of higher rank wants, they can give you a promotion, but only if you are registered. The thing is, if you don't sign in to your chatroom nickname for 30 days, nickserv will unregister your nickname, so only active users stay registered.<br />
<br />
<b>Q. I see !, @, %, and + next to some user's nicknames. What do they mean?</b><br />
<br />
Each symbol represents a rank, and is something like the hierarchy in the chatroom. "!" is Administrator, "@" is Operator, "%" is Half-Operator, and "+" is Voice. The major difference between each rank, is the ability to kick and/or ban users of lower rank. However the upper ranks, such as Operator and Administrator, can change the setting of the channel, as well as the channel topic (It's that bar at the top of the channel display screen).<br />
<br />
<b>Q. What is a trigger?</b><br />
<br />
A trigger is like a codeword that starts an event in the chatroom. In our chatroom, the triggers are: !uno (starts a game of uno), and !trivia (starts an anime trivia with questions spanning several series, including Bleach. Also has questions on Japanese words.).<br />

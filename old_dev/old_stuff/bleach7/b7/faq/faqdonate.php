<br />
<b>Q. What are the donations for? Are we paying you guys to run the site?</b><br />
<br />
Actually, the staff here volunteers for the site. None of the staff members here receive any kind of compensation for their work other than the satisfaction of helping out their fellow Bleach fanatics. All of the donations go towards paying for our server bills. In addition to the main site server, we have six media servers at the time of this writing, which are dedicated to serving media.
<br /><br />
<b>Q. How do you calculate the donation goal?</b><br />
<br />
The donation goal is the total cost of the servers, minus the previous month's ad revenue.
<br /><br />
<b>Q. Can I donate in a currency besides US dollars?</b><br />
<br />
Yes. <a href="?page=supportus">This page</a> contains a list of currencies that we also accept donations from.
<br /><br />
<b>Q. What happens if you don't reach the donation goal? Will the site shutdown?</b><br />
<br />
Most likely not. We are willing to foot part of the bill on occasion, however if we start regularly missing the donation goal, we may be forced to reduce the number of media servers to compensate.<br />
<br />
<b>Q. What happens if you go over the donation goal?</b><br />
<br />
In the case that we receive more donations than are needed to pay for the monthly bill, those donations will be rolled over and count towards the next month's goal.<br />
<br />
<b>Q. Why is the donation goal so high? I know where you can get hosting for less than $10 per month.</b><br />
<br />
Because of the amount of traffic that Bleach7 receives, we are not able to host our site on a shared server. The main Bleach7 webserver costs over $300 per month just by itself. In addition, our media servers easily go through several terabytes per month serving media.<br />
<br />
<b>Q. I just made a donation. How come the amount remaining on the donation tracker hasn't decreased?</b><br />
<br />
The donation tracker is not automated; it is updated by the staff whenever we check the paypal account. Under normal circumstances, your name should appear on the donators list within 24 hours of your donation. If it has been longer than this since your donation, <a href="mailto:webmaster@bleach7.com">send us an email</a>.<br />
<br />
<b>Q. I can't donate. Is there any other way I can help with the site?</b><br /><br />
If you can't make a monetary donation to Bleach7, there are still other ways you can help us! You can spread the word about Bleach7.com and help unite all of the Bleach fanatics. You can also donate your time to help write information for the information section. For more information on helping to write information, visit <a href="?page=faq/faqbleach7">the Bleach7 FAQ</a>.<br />

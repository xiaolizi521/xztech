<b>Q. What is the affiliates program?</b><br /><br />
The affiliates program is a link exchange where we add a link in our affiliates section to your site, and you add a link on your site to Bleach7. This drives traffic to both sites.
<br /><br />
<b>Q. Does my site have to be anime related to become an affiliate?</b><br /><br />
Yes, we only affiliate with anime sites. If your site is not anime related, it is still possible for you to get a link on our site through our sponsored links program. However, under no circumstances will we link to sites containing adult content.
<br /><br />
<b>Q. How do I apply to become an affiliate?</b><br />
<br />
Send an email to <a href="mailto:webmaster@bleach7.com">webmaster@bleach7.com</a> with the link to your website, a general overview of what your site offers, and what your job on the website. Please note that we receive a high volume of requests for affiliation, so we may not be able to respond to all requests. If you don't receive a reply within one week, we probably did not approve your website. Please do not send us more emails asking us asking us why we didn't approve your site. If your site is not approved, you can apply to be in our sponsored links program.<br />

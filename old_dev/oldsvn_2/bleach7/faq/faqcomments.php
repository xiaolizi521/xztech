<b>Q. What is this section for?</b><br />
<br />
For moderators to refer new and problem users to learn the rules of the comments section.<br />
<br />
<b>Q. Okay, so what IS spam?</b><br />
<br />
Useless posts unrelated to the post topic (or general anime).  A post that doesn't have to do with anything, such as a bunch of nonsensical text, a smiley face, or a phrase that doesn't contribute anything such as &quot;w00t&quot; or &quot;i don't know&quot; etc. Ask a moderator for more detailed examples of spam.<br />
<br />
<b>Q. Is there a place on Bleach7 where I can talk about unrelated topics?</b><br />
<br />
Yes, anything unrelated to the post topic can be discussed in the <a href="?page=chat/index">chatroom.</a><br />
<br />
<b>Q. What is a double post?</b><br />
<br />
It's where one user posts twice in a row.<br />
<br />
<b>Q. What's wrong with double posting?</b><br />
<br />
It's not needed seeing as there is an edit button on the upper right side of your comment.<br />
<br />
<b>Q. What is post counting?</b><br />
<br />
When a user says the number of their post inside the post (especially &quot;FIRST!&quot;).<br />
<br />
<b>Q. What's wrong with post counting?</b><br />
<br />
It's unnecessary, as Bleach7 automatically labels each post with its number.  We trust that you can count perfectly well, so there is no need to show us.<br />
<br />
<b>Q. What does my rank mean?</b><br />
<br />
It just give a general idea of how many posts you have made. Optionally, it can be used to showoff how awesome you are.<br />

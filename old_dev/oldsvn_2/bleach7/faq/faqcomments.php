<b>Q. How do I rank up?</b><br />
<br />
Your rank is linked to the number of posts that you have made, so just post more without spamming.<br />
<br />
<b>Q. When do I use a spoiler tag?</b><br />
<br />
When your comment contains information that has happened in the manga, but not in the anime yet.  It is to protect the members who only watch the anime from accidently stumbling upon information that may spoil the anime for them.<br />
<br />
<b>Q. Why should I use a URL tag for my links?</b><br />
<br />
For convenience.  It is a lot easier to tell what a link directs to if you label it.  Also, it helps to keep the comment pages neat, because links that are too long will stretch the comment pages.<br />
<br />
<b>Q. How do I use a tag? [Spoiler, Link, Bold, etc]</b><br />
<br />
To use a tag, look for the corresponding button at the top of the comment type box and click it, then type what you want the tag to effect in between the two tags.  Example: [spoiler]type spoiler text here[/spoiler]<br />
<br />
<b>Q. Why did my websites link get removed from my comment?</b><br />
<br />
Advertising is not allowed in the comments here at Bleach7. Ask a moderator if you're not sure if a certain link can be used in comments.<br />
<br />
<b>Q. Why did my post count get lowered/reset?</b><br />
<br />
You may have repeated an offense of the above rules and the result was a lowered post count.<br />
<br />
<b>Q. Why am I banned from commenting?</b><br />
<br />
You may have broken the above rules more than one time... or you may have just pissed off The Lurking Staffie. Either are valid reasons for a ban.<br />
<br />
<b>Q. Can I apply to become a comment moderator?</b><br />
<br />
Not exactly. We choose the mods based on when we feel we need more to watch over the ever-growing comments. They will be active, law-abiding members, who have been around for a while and know the comment section well.<br />

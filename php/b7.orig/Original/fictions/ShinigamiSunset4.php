3. Same place.  Back to Right Now.<br />
<br />
As Rukia landed, Orihime rounded on her.  “Why? Why, Rukia – chan? He’s hurt!”<br />
<br />
Orahime attempted to walk closer to the Soul Reaper, but was entangled in the massive hug being executed by Rukia’s physical body under the direction of Chappy, the temporary soul replacement occupying the shell.  “Pyuh!” the Rukia said, beaming.<br />
<br />
Looking slightly disconcerted, shinigami Rukia continued to gaze upwards.  “This has become an honor – battle that Ichigo must fight alone, with no help from me.”  Her eyes widened and her lips compressed as she saw and felt the massive blow Ichigo and Zangetsu delivered. Orihime gasped and raised her eyes as well.  “You,” Rukia said, pointing, “are not a shinigami, and have no such restriction…”<br />
<br />
“Yo!” They heard Ichigo’s voice, sounding somewhat desperate. “Orahime!” <br />
<br />
“Tsubaki…” Orahime said in one breath. “…Koten Zanshun… I REJECT!”<br />
<br />
“YES!” The shunshun rikka screamed as his black and yellow form knifed upwards in a shrieking blur.<br />
<br />
The explosion of green and yellow fluid resulting from the bisection of the hollow evaporated, drawn into the Balance, before it could do more than flavor the air with the putrid stench of a soul’s putrefaction.<br />
<br />
<br />
* <br />
<br />
“What…” Ichigo started as he gingerly landed on the street, “what was that two – for – one bastard?”<br />
<br />
“A symbiote hollow, also called a Twinner.  Only one has ever been seen previous to today.  Two souls with a link so strong that it is maintained even after death.” Rukia frowned. “There’s no real way to tell the second hollow is present because the two are wrapped into one skin.”<br />
<br />
"Hey, what is... what's going on here!?" The two turned to the mouth of the alley, where a grey - tweeded businessman had stopped in slack - jawed amazement. “What are you doing with those girls!”  He raised his briefcase as a weapon, his head turning as if looking for help.<br />
<br />
"Spirit - sensitive..." Rukia said.<br />
<br />
"Yeah, yeah..." Ichigo replied crossly.  "Hey," he said to the businessman, a thin and unconvincing grin writing on his bloody face. "If you don't mind, we're trying to rehearse here for our school play..." He motioned with his hand, sending scarlet drops flying from his fingertips.<br />
<br />
The man's eyes moved over the spiraling shunshun - rikka, lingered on Orahime's breasts and fixed finally on the hilt extending over Ichigo's shoulder. He frowned, unconvinced. Orihime folded her arms over her chest, eyes troubled.<br />
<br />
Rukia elbowed Ichigo hard in the ribs, doubling him over and forcing him out of her way.  “Misuta – sama,” she said, smiling, please look at my script…” She raised her hand, and the man’s eyes focused on the object there.<br />
<br />
“BOOM.”  The object exploded in her fist, leaving only a bobbing chicken - head – on - a – spring.  Ichigo turned his grimacing face up and smirked. “Kioku – chikan memory replacement…!”<br />
<br />
<br />
"Oh!" The man said, his briefcase sliding down and face relaxing.  "Sorry, my mistake."  He started to walk away, then stopped.  "What an odd..?" He looked up and met Ichigo's eyes, frowned and then continued on.<br />
<br />
“Maybe,” Ichigo said, returning to the subject of their previous conversation, ”…maybe someone should come up with a way to figure out if there are two bastards in a hollow body.  Does that sound unreasonable?”  He put his hand against the alley wall and stopped, silent but for the plip – plop of blood falling onto the street from his wounds.  “I’m, ah, going to rest for a minute…”<br />
<br />
<br />
* <br />
<br />
Ichigo sat, Zangetsu across his crossed knees, back against the cold alley wall, his expression alternating between a wince at the pain he could now allow himself to feel and a scowl at his two female companions. “Would someone,” he said, tiredly, “please make up their mind?  Please?”  His eyes rolled upwards.  “Getting dark, here…!”<br />
<br />
The ladies ignored him.  The shunshun - rikka Lily, however, zoomed over to hover before his face; she gave him a huge and suggestive wink and slapped twice at the tattoo on the exposed section of her posterior.  Ichigo recoiled, reddening and blinking his eyes several times in disbelief.  “O – orahime…?” He stuttered.<br />
<br />
“No, really, my Soten Kishun should do the trick.” Orihime motioned with her hands and smiled at Rukia. "Just a minute, Kurosaki - kun."<br />
<br />
“Perhaps.  My Kido will do the job…” Rukia stated, tapping at her cheek. She had returned to her physical form and one hand was jammed into the front pocket of her black jeans.<br />
<br />
“I really feel my healing reflection shield is the best way.  Look how it took care of Jidanbo’s arm..!”<br />
<br />
“From what I hear, that took days, though.  Kido is much quicker, Orahime - kun.”<br />
<br />
“Yo!  I’m starting to feel lightheaded here…”<br />
<br />
Both women turned towards him, and then turned away dismissively.<br />
<br />
"Erf." Ichigo sighed and attempted to shrug, then grimaced and frowned at the resulting pain. His eyebrows and the corner of his mouth began to twitch. <br />
<br />
“This sucks.” He grumbled.<br />

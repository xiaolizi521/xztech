<br />
The world was quiet, save for the distant rumbling of thunder and the howling winds.<br />
<br />
The world was still, save for the dark clouds racing across the sky.<br />
<br />
The man dressed in black stared across the deserted city, letting the winds whip through his long black hair and snap his clothes against his body.  It was a curious sensation, standing here alone among the buildings and trees that dotted his view.  But one that he was used to.  Whenever he was here, he was alone.<br />
<br />
No, not alone.  There was the other one, sometimes here and sometimes...elsewhere.  He frowned then, remembering that parting comment.<br />
<br />
'Train him well... his power will be mine soon enough.'<br />
<br />
He had to admit, though, that that might very well be true.  Or perhaps it was just arrogant boasting on the other's part.<br />
<br />
Even he, he admitted, did not know all the secrets of a soul.  Even his own.<br />
<br />
Ah, but he was a rash one, this time 'round.  Impetuous, ignorant as one measures such things, and woefully short of wisdom and experience.  Still, all these could be corrected -- the basic metal, of course, was sound.  And pride... well, by his estimation pride was not a flaw.  In time, if he survived, he would again be foremost among the balancers of souls, for good or ill. Or however such things were measured.<br />
<br />
But...<br />
<br />
The other troubled him.  HIM him, not his present self.  Bah! Words were cumbersome, but he knew what he meant.  The other was still... himself.  But not.  The pride was still there, but exaggerated.  And the underlying metal... was tarnished, warped.  But still strong.  Always strong.<br />
<br />
It was odd, really... the past, present, and future of himself, he supposed, to be poetic about it.  He was used to one of himself, or two.  Three was dangerous and unbalanced.<br />
<br />
He shrugged and sighed, gazing again at the distant horizon.  What will be, will be, he supposed.<br />
<br />
But he still wondered... if one unchained soul was a hollow, and two was a shinigami, then what was three?<br />
<br />
*****<br />
<br />
The dark man found himself staring again at the sky.  The light this time was a swirling maelstrom of blues, reds, and purples.  No clouds raced towards the horizon, but the wind howled as it whipped around him.  There was a tingling feeling in the air and the smell of ozone was thick.<br />
<br />
He was aware of the fight, of course; after all, he was one of the combatants. It was a fierce and bitter duel -- a part of him vibrated with the excitement that made him twist his face into a vicious grin.  He had never been one to seek combat... but it often sought him out any way, and over time he'd grown to enjoy the challenge of it.<br />
<br />
But he still felt... disappointment in his opponent.  Not at his skill -- no, he could find little fault with the other man's ability with a blade or with his control of his spiritual energy.<br />
<br />
But he never thought he'd be fighting the balancers.  Never.  He'd always seen those who opposed the shikigami as misguided at best... and traitors at worst.<br />
<br />
But then, he thought, his grin twisting further towards a grimace, the world looks different from the other side, doesn't it?  Now, he had little doubt, he was he who was seen as a dangerous menace to the two worlds.<br />
<br />
He called up memories of his sneers in days long past, his biting contemptuous words, all the surety of his own righteousness displayed in offended pride.<br />
<br />
Seeing things as an outsider, as he did this time, he could clearly see the rot which he had overlooked before.  It was a slow and subtle thing, made more of hubris and ignorance than malice and ambition.<br />
<br />
"We had seen ourselves first as protectors and avengers, and then as defenders and guardians.  Time passed, and we started to see ourselves as necessities and overlords... and, finally, as gods.  The shikigami, the gods of death. Hah!"<br />
<br />
He spoke aloud to the empty sky, spitting his words out in some vain hope of being proven wrong.  But only the wind answered, wordlessly.  And its answer was neither pleasing nor compassionate.<br />
<br />
He could feel his frustration at such a colossal failure on his part -- his blindness -- well up, and he suppressed the urge to scream at the wisps of color that trailed towards the horizon.  All that pain, the dead and the broken... the sacrifice of so many... just to place comfortable tyrants on a pedestal where they could stare benevolently across their domain.  They had never wanted to create such a thing!<br />
<br />
"We... we had just wanted to make the world safer.  So that there would be no more like us, driven only by thoughts of revenge against the never-ending tide of the Hungry.  But not like this... never like this..."<br />
<br />
He drew a deep slow breath and let it out, watching the sky in silent reflection of long-past glories and horrors.  Now was not the time for this... the air was getting thicker with suppressed energy.  He could feel the stretch as he reached for himself... and, for a moment, was truly whole.<br />
<br />
"Ban... Kai..."<br />
<br />
And he vanished, just as the sky erupted in corruscating lightning.<br />

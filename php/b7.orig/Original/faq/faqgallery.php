<b>Q. How good does my submission have to be to get approved?</b><br />
<br />
Our criteria for approving a submission is not based on the artistic ability of the submitter. As long as your submission is Bleach related and does not contain inappropriate content, your submission will be approved.
<br /><br />
<b>Q. Why does my submission need to be approved before going into the gallery?</b><br />
<br />
To ensure that only appropriate content that is related to Bleach is displayed.
<br /><br />
<b>Q. How do I know if my submission was approved or rejected?</b><br />
<br />
In the future we will add a feature that will send you a pm if your submission was rejected, and the reason for doing so. For now, under normal circumstances, your submission should be approved within 12 hours. If you do not see your submission in the gallery after that timeframe, it is likely that your submission was denied. If you are unsure, you can pm one of the staff to find out.<br />
<br />
<b>Q. What formats can I submit my image in?</b><br />
<br />
The file formats we support currently are .jpg .png and .gif.<br />
<br />
<b>Q. Are there any limitation on how big my submission can be?</b><br />
<br />
There is no limit on the resolution of the image, however there is a limit on the file size. Wallpaper submissions must be smaller than 2 MB, fan art submissions must be smaller than 800 KB, and submissions for the general gallery must be smaller than 600 KB.<br />
<br />
<b>Q. What do I do if the file size of my submission is over the limit?</b><br />
<br />
The first thing you should try is converting your image to JPG format. PNG and GIF are lossless formats which can cause the file to be quite large, however compressing it as a JPG can help reduce the file size without noticeably impacting quality. If your file is still too large, you can reduce the image resolution to lessen the file size.<br />
<br />
<b>Q. Which category do I submit my image to?</b><br />
<br />
If your submission is meant to be used as a desktop background, submit it to the <a href="?page=media/wallsubmit">wallpaper gallery</a>. If you submissions is an original Bleach drawing not done by Kubo Tite, or is a coloring, submit it to the <a href="?page=media/fansubmit">fan art gallery</a>. Any other types of images such as avatars or pictures of Bleach cosplay can be submitted to the <a href="?page=media/imagesubmit">general Bleach gallery</a>.<br />
<br />
<b>Q. What are kudos?</b><br />
<br />
Kudos are a general indicator of how other fans like your image. You can give an image anywhere from one to three kudos.<br />
<br />
<b>Q. Am I allowed to submit an image that I didn't create?</b><br />
<br />
Yes, as long as you credit the original artist in the description or simply state the work is not yours if the artist is unknown.<br />

<h2> General Requirements </h2>
<p> Javascript Enabled </p>
<p> Monitor Resolution: 1024 x 768 (16 bit color or higher) </p>

<h2> Firefox </h2>

<p>
Firefox is a browser built by the
<a href="http://mozilla.org/">Mozilla</a> project.  It has two great features:
it is fully compliant with HTML and CSS <a href="http://w3c.org/">standards</a>, 
and it is NOT written by Microsoft. Firefox 3 is only in "Release Candidate" status and is not supported by CORE at this time.

</p>

<h3> Instructions for updating/installing Mozilla for Windows. </h3>

<p>
If you don't know how to install Firefox, or can't install software on
your machine (ie. you have a managed desktop), contact the helpdesk.

<h3> Instructions for updating/installing Firefox for Linux. </h3>

<p>
Firefox is installed by default on most modern Linux installations that
have X windows.  Since the helpdesk doesn't really support Linux, you
probably had to install it yourself.  If you were able to do that, then you
should be able to install Firefox if it isn't there already.  If you don't
know how to manage your Linux installation, then call the helpdesk and get
them to bring you a Windows box so they can help you.  They're really good with
Windows and can get you taken care of in a flash.
</p>

<h2> Internet Explorer </h2>

<p>
Internet Explorer needs to be version 6.0.2800 or newer of the version 6 family.  
If it is older, please contact the helpdesk to have your browser updated.  Don't
try to run it on Internet Explorer 7, because there are a host of known issues 
with IE 7 that aren't on anyone's radar to be fixed anytime soon.  Personally, I
(and a few others) think we should support IE7 because it's the default browser
for most modern Windows installations, but everyone else seems to think that if
you are running IE, then you deserve whatever you get.
</p>

